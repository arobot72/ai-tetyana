function renderProductTags(tags) {
    const container = document.getElementById('product-tags');
    if (!container) return;
    container.innerHTML = tags.map(t => `<span class="tag">${t} <span class="tag-remove" onclick="removeProductTag('${t}')">×</span></span>`).join('');
}

function addProductTag() {
    const input = document.getElementById('new-product-tag');
    const tag = input.value.trim();
    if (tag) {
        const fullTag = tag.startsWith('#') ? tag : '#' + tag;
        const currentTags = Array.from(document.querySelectorAll('#product-tags .tag')).map(t => t.textContent.replace('×', '').trim());
        if (!currentTags.includes(fullTag)) {
            currentTags.push(fullTag);
            renderProductTags(currentTags);
        }
        input.value = '';
    }
}

function removeProductTag(tag) {
    const currentTags = Array.from(document.querySelectorAll('#product-tags .tag')).map(t => t.textContent.replace('×', '').trim()).filter(t => t !== tag);
    renderProductTags(currentTags);
}

function saveProduct(e) {
    e.preventDefault();
    const form = e.target;
    const lang = localStorage.getItem('adminLang') || 'ua';
    const tags = Array.from(document.querySelectorAll('#product-tags .tag')).map(t => t.textContent.replace('×', '').trim());
    const existingProduct = currentProductId ? cmsData.products.find(p => p.id === currentProductId) : null;
    
    const product = {
        id: currentProductId || Date.now(),
        title: existingProduct ? { ...existingProduct.title } : { ua: '', en: '', ru: '' },
        description: existingProduct ? { ...existingProduct.description } : { ua: '', en: '', ru: '' },
        details: existingProduct ? { ...existingProduct.details } : { ua: '', en: '', ru: '' },
        mainImage: form.mainImage.value,
        price: parseInt(form.price.value) || 0,
        rating: parseFloat(form.rating.value) || 4.5,
        tags: tags,
        detailed: form.detailed.checked,
        center: 'left',
        images: []
    };
    product.title[lang] = form.title.value;
    product.description[lang] = form.description.value;
    product.details[lang] = form.details.value;
    
    if (currentProductId) {
        const index = cmsData.products.findIndex(p => p.id === currentProductId);
        if (index !== -1) cmsData.products[index] = product;
    } else {
        cmsData.products.push(product);
    }
    localStorage.setItem('cmsData', JSON.stringify(cmsData));
    loadAdminData();
    alert('Товар збережено!');
}

