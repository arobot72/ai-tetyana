// App State
let currentLang = 'ua';
let currentPage = 'home';
let currentSlide = 0;

// Initialize app
document.addEventListener('DOMContentLoaded', () => {
    loadPage('home');
    initLanguageSwitcher();
    initMobileMenu();
    initCarousel();
    initCookies();
    renderFooterLinks();
});

// Language Switcher
function initLanguageSwitcher() {
    // Using select dropdown now, no need for buttons
    const select = document.getElementById('lang-select');
    if (select) select.value = currentLang;
}

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);
    const select = document.getElementById('lang-select');
    if (select) select.value = lang;
    applyTranslations();
    loadPage(currentPage);
}

function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        const keys = key.split('.');
        let value = translations[currentLang];
        keys.forEach(k => {
            value = value ? value[k] : null;
        });
        if (value) el.textContent = value;
    });
}

// Mobile Menu
function initMobileMenu() {
    const burger = document.getElementById('burger');
    const nav = document.getElementById('nav');
    burger.addEventListener('click', () => {
        nav.classList.toggle('active');
        burger.classList.toggle('active');
    });
}

// Carousel
function initCarousel() {
    setInterval(() => {
        currentSlide = (currentSlide + 1) % 5;
        updateCarousel();
    }, 3000);
}

function updateCarousel() {
    document.querySelectorAll('.hero-slide').forEach((slide, index) => {
        slide.classList.toggle('active', index === currentSlide);
    });
    document.querySelectorAll('.slider-dot').forEach((dot, index) => {
        dot.classList.toggle('active', index === currentSlide);
    });
}

// Cookies
function initCookies() {
    if (!localStorage.getItem('cookiesAccepted')) {
        document.getElementById('cookie-banner').classList.add('active');
    }
}

function acceptCookies() {
    localStorage.setItem('cookiesAccepted', 'true');
    document.getElementById('cookie-banner').classList.remove('active');
}

// Footer Links
function renderFooterLinks() {
    const services = cmsData.products.filter(p => p.tags.includes('#Послуга'));
    const footerServices = document.getElementById('footer-services');
    if (footerServices) {
        footerServices.innerHTML = services.map(s => 
            `<li><a href="#services">${s.title[currentLang]}</a></li>`
        ).join('');
    }
}

// Modal Functions
function openModal(type, data = null) {
    const modal = document.getElementById('modal');
    const modalBody = document.getElementById('modal-body');
    
    if (type === 'contact') {
        modalBody.innerHTML = `
            <h3>${currentLang === 'ua' ? 'Записатися на консультацію' : currentLang === 'en' ? 'Sign up for consultation' : 'Записаться на консультацию'}</h3>
            <form onsubmit="submitForm(event)">
                <div class="form-group">
                    <label>${translations[currentLang].form.name}</label>
                    <input type="text" name="name" required>
                </div>
                <div class="form-group">
                    <label>${translations[currentLang].form.phone}</label>
                    <input type="tel" name="phone" required>
                </div>
                <div class="form-group">
                    <label>${translations[currentLang].form.email}</label>
                    <input type="email" name="email">
                </div>
                <div class="form-group">
                    <label>${translations[currentLang].form.message}</label>
                    <textarea name="message"></textarea>
                </div>
                <button type="submit" class="btn btn-primary">${translations[currentLang].form.submit}</button>
            </form>
        `;
    } else if (type === 'product' && data) {
        modalBody.innerHTML = `
            <h3>${data.title[currentLang]}</h3>
            <div class="product-details">
                ${data.details[currentLang]}
            </div>
            <div class="product-price">${data.price} грн/${currentLang === 'ua' ? 'год' : currentLang === 'en' ? 'hour' : 'час'}</div>
            <button class="btn btn-primary" onclick="closeModal(); openModal('contact')">${translations[currentLang].nav.cta}</button>
        </div>
        `;
    }
    
    modal.classList.add('active');
}

function closeModal() {
    document.getElementById('modal').classList.remove('active');
}

// Close modal on outside click
document.getElementById('modal').addEventListener('click', (e) => {
    if (e.target.id === 'modal') closeModal();
});

// Form Submit
function submitForm(e) {
    e.preventDefault();
    alert(translations[currentLang].form.success);
    closeModal();
}

// Page Router
function loadPage(page) {
    currentPage = page;
    const app = document.getElementById('app');
    const t = translations[currentLang];
    
    switch(page) {
        case 'home':
            app.innerHTML = renderHomePage(t);
            initCarousel();
            break;
        case 'services':
            app.innerHTML = renderServicesPage(t);
            break;
        case 'courses':
            app.innerHTML = renderCoursesPage(t);
            break;
        case 'about':
            app.innerHTML = renderAboutPage(t);
            break;
        case 'philosophy':
            app.innerHTML = renderPhilosophyPage(t);
            break;
        case 'methods':
            app.innerHTML = renderMethodsPage(t);
            break;
        case 'contacts':
            app.innerHTML = renderContactsPage(t);
            break;
    }
    
    window.scrollTo(0, 0);
}

